package arrey.example;

public class Employee {
	private int empid;
	private String Name;
	 
	public Employee() {}

	public Employee(int empid, String name) {
		super();
		this.empid = empid;
		Name = name;
	}

	public int getEmpid() {
		return empid;
	}

	public void setEmpid(int empid) {
		this.empid = empid;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	
	}
	
	


