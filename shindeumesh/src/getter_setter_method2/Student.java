package getter_setter_method2;


public class Student {
	private String name;
	private String shcoolNme;
	private Addres adr;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getShcoolNme() {
		return shcoolNme;
	}

	public void setShcoolNme(String shcoolNme) {
		this.shcoolNme = shcoolNme;
	}

	public Addres getAdr() {
		return adr;
	}

	public void setAdr(Addres adr) {
		this.adr = adr;
	}

	
}
