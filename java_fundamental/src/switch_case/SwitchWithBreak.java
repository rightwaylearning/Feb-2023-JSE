package switch_case;

public class SwitchWithBreak {

	public static void main(String[] args) {

		int arg = 20;

		switch (arg) {

					default: {
						System.out.println("default");
						break;
					}
					case 0: {
						System.out.println("0");
						break;
					}
			
					case 1: {
						System.out.println("1");
						break;
					}
			
					case 2: {
						System.out.println("2");
						break;
					}

		}

	}
}
