package switch_case;

public class SwitchCase {

	public static void main(String[] args) {
		int num = 170;
		
		switch(num) {
		
		case 10:{
			System.out.println("match is found for 10");
			break;
		}
		case 20:{
			System.out.println("match is found for 20");
			break;
		}
		case 30:{
			System.out.println("match is found for 30");
			break;
		}
		case 40:{
			System.out.println("match is found for 40");
			break;
		}
		case 50:{
			System.out.println("match is found for 50");
			break;
		}
		case 60:{
			System.out.println("match is found for 60");
			break;
		}
		case 70:{
			System.out.println("match is found for 70");
			break;
		}
		case 80:{
			System.out.println("match is found for 80");
			break;
		}
		case 90:{
			System.out.println("match is found for 90");
			break;
		}
		case 100:{
			System.out.println("match is found for 100");
			break;
		}
		default:{
			System.out.println("no match found");
		}
	  }
	}
}
