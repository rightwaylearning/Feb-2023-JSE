package operators_control_flow;

public class Downcasting {
public static void main(String[] args) {
		
		byte i2 = 4;
		
		int i3 = (int)i2;
		
		System.out.println(i3);

		
		int i = 134;
		
		byte b = (byte) i;
		
		System.out.println(b);
		
 
		
		
		float f = 43.43f;
		
		int i1 = (int) f;
		
		System.out.println(i1);
		
 
		
		
		char ch = 'a';
		int a = (int) ch;
		
		char ch1 = (char) a ;
		
		System.out.println(a);
		System.out.println(ch1);
		
	}



}
