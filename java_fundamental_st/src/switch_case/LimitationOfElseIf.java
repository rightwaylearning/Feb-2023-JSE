package switch_case;

public class LimitationOfElseIf {

	public static void main(String[] args) {

		int num = 100;
		if (num == 10) {
			System.out.println("match is not found for 10");
		} else if (num == 20) {
			System.out.println("match is not found for 20");
		} else if (num == 30) {
			System.out.println("match is not found for 30");
		} else if (num == 40) {
			System.out.println("match is not found for 40");
		} else if (num == 50) {
			System.out.println("match is not found for 50");
		} else if (num == 60) {
			System.out.println("match is not found for 60");
		} else if (num == 70) {
			System.out.println("match is not found for 70");
		} else if (num == 80) {
			System.out.println("match is not found for 80");
		} else if (num == 90) {
			System.out.println("match is not found for 90");
		} else if (num == 100) {
			System.out.println("match is not found for 100");
		} else {
			System.out.println("I am sorry match is not found");
		}
	}
	
}
