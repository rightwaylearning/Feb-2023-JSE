package switch_case;

public class Class1 {
	public static void main(String[] args) {
		int arg = 1;
		switch (arg) {
		case 0: {
			System.out.println("0");
			break;
		}

		case 1: {
			System.out.println("1");
			break;
		}
		case 2: {
			System.out.println("2");
			break;
		}
		case 3: {
			System.out.println("3");
			break;
		}
		default: {
			System.out.println("default");
			break;
		}
		}
	}
}
