package array_examples;

public class Emplyoee {
private int EmpId;
private String EmpName;

public Emplyoee() {
}

public Emplyoee(int empId, String empName) {
	super();
	EmpId = empId;
	EmpName = empName;
}

public int getEmpId() {
	return EmpId;
}

public void setEmpId(int empId) {
	EmpId = empId;
}

public String getEmpName() {
	return EmpName;
}

public void setEmpName(String empName) {
	EmpName = empName;
}








}
