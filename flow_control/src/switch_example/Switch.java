package switch_example;

public class Switch {
	  
	public static void main(String[] args) {
		
	
	 String name = "virat";
	 
	switch(name) {
	
	case("rohit"):{
		System.out.println("i am rohit");
		break;
	}
	case("dhoni"):{
		System.out.println("i am dhoni");
		break;
	}
	case("jadeja"):{
		System.out.println("i am jadeja");
		break;
	}
	default:{
		System.out.println("sorry...");
		break;
	}
	
	}

  }
	

}
