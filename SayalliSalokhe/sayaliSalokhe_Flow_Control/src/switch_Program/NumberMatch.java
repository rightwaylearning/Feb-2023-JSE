package switch_Program;

public class NumberMatch {
	public static void main(String[] args) {
		int num = 110;
		switch (num) {
		case 10: {
			System.out.println("numer is 10");
			break;
		}
		case 70: {
			System.out.println("numer is 70");
			break;
		}
		case 170: {
			System.out.println("numer is 170");
			break;
		}
		case 99: {
			System.out.println("numer is 99");
			break;
		}
		default: {
			System.out.println("pass wrong number");
		}

		}
	}

}
