package switch_Program;

public class NameMatch {
	public static void main(String[] args) {
		String ch = "nita";
		switch (ch) {
		case "sayali": {
			System.out.println("Name is sayali");
			break;
		}
		case "rupa": {
			System.out.println("Name is rupa");
			break;
		}
		case "nita": {
			System.out.println("Name is nita");
			break;
		}
		case "sheela": {
			System.out.println("Name is sheela");
			break;
		}
		default: {
			System.out.println("pass wrong name");
			break;
		}
		}
	}

}
