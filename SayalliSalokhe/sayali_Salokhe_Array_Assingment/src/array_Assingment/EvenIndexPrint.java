package array_Assingment;

public class EvenIndexPrint {
	public static void main(String[] args) {
		int[] array= {10,30,50,89,22};
		int index=0;
		

	
		while(index<array.length)
		{
	       System.out.println("Even No:"+array[index]);
	      index= index+2;
	      
			
		}
		
		
	}

}
