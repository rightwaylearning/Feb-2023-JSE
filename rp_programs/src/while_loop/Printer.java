package while_loop;

public class Printer {
	
	 public static void m1(int[] num) { 
		 
//		 int[] arr = new int[];
		 
		 int i = 0 ;
		 
		 while(i < num.length) {
			 System.out.println(num[i]);
			 i++;
		 }
		 
		 System.out.println("<<<<<<<<"+i);
		 
		 System.out.println("<<<<<<<<<<<<<>>>>>>>>>>>>>");
	 }

}
