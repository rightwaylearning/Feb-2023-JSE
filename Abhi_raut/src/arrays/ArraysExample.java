package arrays;

public class ArraysExample {
    // int[] - these int array is reference type method  
	
	public static void main(String[] args) {
		
		int[] a = new int[4];
		
		a[0]= 50;
		a[1]= 100;
		a[2]= 150;
		a[3]= 200;
		
		System.out.println(a[0]);
		System.out.println(a[1]);
		System.out.println(a[2]);
		System.out.println(a[3]);
		
	}
}
