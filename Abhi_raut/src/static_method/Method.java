package static_method;

public class Method {
	
	// class have 2 types of method 
	// 1) instance method or object method (non-static method)
	// 2) static method 
	
	
	void m1 () {
		
		System.out.println("hi these is instance method ");
		
	}
   
	// static method
	static void m2 () {
		
		System.out.println("hi these is static method");
	
	}
}
