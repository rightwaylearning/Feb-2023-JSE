package array_example;

public class Employee {

	
	private int Eid;
	private String Ename;
	
	public Employee() {
		
	}
	
	public Employee(int Eid, String Ename) {
		super();
		this.Eid = Eid;
		this.Ename = Ename;
	}
	
	public int getEid() {
		return Eid;
		
	}
	public void setEid(int Eid) {
		this.Eid = Eid;
		
	}
	public String getEname() {
		return Ename;
		
	}
	public void setEname() {
		this.Ename = Ename;
		
	}
	
	
    
		
	}

