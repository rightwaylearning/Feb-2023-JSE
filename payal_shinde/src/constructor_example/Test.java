package constructor_example;

public class Test {


		Test(){      //this is method,constructor
			System.out.println("Hi! I am zero parameterized constructor");
		}
		
		Test(int a, int b){      //parametrized method
			System.out.println("I have 2 parameters");
		}
		
		Test(int z, int y, int m){
		   System.out.println("I have 3 parameters");	
		}
	}


