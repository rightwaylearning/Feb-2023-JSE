package array_example;

public class PrimitiveArray {
	
	public static void main(String[] args) {
		
		int[] arr = new int[10];
		
		arr[0] = 10;
		arr[1] = 11;
		arr[2] = 12;
		arr[3] = 13;
		arr[4] = 14;
		arr[5] = 15;
		arr[6] = 16;
		arr[7] = 17;
		arr[8] = 18;
		arr[9] = 19;
		
		System.out.println(arr[5]);
		System.out.println(arr[4]);
		System.out.println(arr[8]);
	}

}
