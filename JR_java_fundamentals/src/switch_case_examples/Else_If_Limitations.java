package switch_case_examples;

public class Else_If_Limitations {
	
	public static void main(String[] args ) {
		
		int num=5;
		
		if(num == 1) {
			System.out.println("Match is found for 1");
		} else if (num == 2) {
			System.out.println("Match is found for 2");
		}else if (num == 3) {
			System.out.println("Match is found for 3");
		}else if (num == 4) {
			System.out.println("Match is found for 4");
		}else if (num == 5) {
			System.out.println("Match is found for 5");
		}else if (num == 6) {
			System.out.println("Match is found for 6");
		}else {
			System.out.println(" Sorry Match Number Not Found");
			
		}
		
	}

}
