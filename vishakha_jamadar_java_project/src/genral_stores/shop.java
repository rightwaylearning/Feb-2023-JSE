package genral_stores;

public class shop {
	private String name;
	private int id;
	private address adr;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public address getAdr() {
		return adr;
	}

	public void setAdr(address adr) {
		this.adr = adr;
	}

}
