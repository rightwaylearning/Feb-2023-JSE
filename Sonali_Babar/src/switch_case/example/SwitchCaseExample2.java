package switch_case.example;

public class SwitchCaseExample2 {
	public static void main(String[] args) {
		String name = "Snehal";

		switch (name) {
		case "Snehal": {
			System.out.println("I am Snehal");
			break;
		}
		case "Sakshi": {
			System.out.println("I am Sakshi");
			break;
		}
		case "Radhika": {
			System.out.println("I am Radhika");
			break;
		}
		case "Sonali": {
			System.out.println("I am Sonali");
			break;
		}
		default: {
			System.out.println("Name is Not Matched");

		}
		}
	}

}
