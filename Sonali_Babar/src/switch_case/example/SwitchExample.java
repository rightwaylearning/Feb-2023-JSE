package switch_case.example;

public class SwitchExample {
	public static void main(String[] args) {
		int num = 30;

		switch (num) {
		case 10: {
			System.out.println("Number is Matchted to 10 ");
			break;
		}
		case 20: {
			System.out.println("Number is Matchted to 20 ");
			break;
		}
		case 30: {
			System.out.println("Number is Matchted to 30 ");
			break;
		}
		case 40: {
			System.out.println("Number is Matchted to 40 ");
			break;
		}
		case 50: {
			System.out.println("Number is Matchted to 50 ");
			break;
		}
		case 60: {
			System.out.println("Number is Matchted to 60 ");
			break;
		}
		default: {
			System.out.println("Matched Not Found");
		}

		}

	}

}
