package all.operators;

public class RelationalOperator 
{
	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		System.out.print("a=10 > b=20 :-");
		System.out.println(a>b);
		
		System.out.print("20 < 10 :-");
		System.out.println(20<10);
		
		System.out.print("20 > 10 :-");
		System.out.println(20>10);
		
		System.out.print("20 <= 20 :-");
		System.out.println(20<=20);
		
		System.out.print("20 >= 20 :-");
		System.out.println(20>=10);
		
		System.out.print("20 <= 20.0f :-");
		System.out.println(20<=20.0f);
		
		System.out.print("20 >= 20 .00:-");
		System.out.println(20>=20.00);
		
		System.out.print("A <= 65 :-");
		System.out.println('A'<=65 );
		
		System.out.print("A+1 >= B :-");
		System.out.println('A'+1 >= 'B');
		
		
	}

}
